# create a lists using []
a = [0, 1, 8, 7, 4]
print(a)

# access using index using a[0], a[1]
print(a[2])

# change the value of list
a[0] = 98
print(a)

# we can create a list of different datatypes
b = [45, 'harsh', 67.9]
print(b)

# Lists slicing
friends = ["prateek", "himanshu", "ashish", 44, "divya"]
print(friends[0:3])
